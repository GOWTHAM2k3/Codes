class colors
{
public static void main(String[] args)
{
String color="red";
switch(color)
{
case "blue":
{
System.out.println("blue");
}
case "yellow":
{
System.out.println("yellow");
}
case "green":
{
System.out.println("green");
}
case "pink":
{
System.out.println("pink");
}
case "red":
{
System.out.println("red");
}
default:
{
System.out.println("default");
}
}
}
}

